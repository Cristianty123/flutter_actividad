class Movie {
  final int id;
  final String title;
  final String description;
  final int year;
  final String imageUrl;
  final String genre;
  final int stars;

  Movie({
    required this.id,
    required this.title,
    required this.description,
    required this.year,
    required this.imageUrl,
    required this.genre,
    required this.stars,
  });


}